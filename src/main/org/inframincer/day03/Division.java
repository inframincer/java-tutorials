package org.inframincer.day03;

public class Division {

    public static void main(String[] args) {
        int a = 10; // 정수
        int b = 3; // 정수

        float c = 10.0F; // 실수
        float d = 3.0F; // 실수

        System.out.println(a / b);
        System.out.println(c / d);
        System.out.println(a / d);
    }
}
