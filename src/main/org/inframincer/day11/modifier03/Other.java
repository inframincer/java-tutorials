package org.inframincer.day11.modifier03;

public class Other {

    public void publicMethod() {
        System.out.println("other class public method");
    }

    protected void protectedMethod() {
        System.out.println("other class protected method");
    }

    void defaultMethod() {
        System.out.println("other class default method");
    }

    private void privateMethod() {
        System.out.println("other class private method");
    }
}
